    private void tombolSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolSimpanActionPerformed
        // TODO add your handling code here:
        String sql = "insert into siswa values (?,?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, nama.getText());
            stat.setString(2, nisn.getText());
            String jkel="";
            if (jk1.isSelected()) jkel="Laki-Laki";
            else jkel = "Perempuan";
            stat.setString(3, jkel);

            stat.setString(4,alamat.getText());
            stat.setString(5,kelas.getSelectedItem().toString());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            kosong();
            nisn.requestFocus();
            datatable();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan"+e);
        }
    }//GEN-LAST:event_tombolSimpanActionPerformed

